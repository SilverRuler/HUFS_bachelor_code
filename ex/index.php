▶뱀띠= 허영심으로 뜬구름 잡는 자만심을 버려라. 가정에서 외면당하고 자신도 초라해질 운. ㅇㅊㅂ성씨 금전거래하다 관재수 있겠으니 4, 7, 11월생과는 거래를 자중할 것. 힘든 일은 제일 가까운 개, 닭띠와 의논하면 풀리는 길이 열릴 듯.

