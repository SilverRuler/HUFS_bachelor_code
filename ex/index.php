▶소띠= 남을 의지하려다 사탕발림에 현혹되어 곤경에 빠질 운. 현재는 힘에 겨우나 자신있게 실력으로 추진하면 승산이 있을 듯. 73년 1, 2, 11월생 마음에서 떠난 사람 미련 두지 말라. 1, 2, 11월생 개척이 급선무. 잡념을 버리면 동·북쪽에 행운 있다.

▶범띠= 건강으로 인해 화목한 가정에 우환이 닥칠 수. 각별히 건강에 조심하고 외출은 삼감이 좋을 듯. 4, 9, 12월생 기름진 것이나 회 종류의 음식은 피할 것. 86년생 ㅇㅊㅂ성씨 아기의 잉태를 보려면 아내만 구박 말고 자신도 병원의 문을 두드려라.

